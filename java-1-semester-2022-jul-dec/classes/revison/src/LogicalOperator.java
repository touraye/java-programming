public class LogicalOperator {
    public static void main(String[] args) {
        /*
            Logical Operator
            &&  AND
            ||  OR
            !   NOT
         */
        System.out.println("True && False: " + (true && false));
        System.out.println("True && True: " + (true && true));
        System.out.println("False && False: " + (false && false));
    }
}
